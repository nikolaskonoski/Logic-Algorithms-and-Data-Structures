# Listas em Python
# Em python podemos colocar varios tipos de dados em um array
# Mas o ideal é que todos os dados sejam do mesmo tipo

# Aqui estamos iterando e somando os valores
idades = [15, 46, 75, 34, 23]
total = 0
for idade in idades: #loop de iteração 
    total = total + idade
print(total)